function adicionarDia(eventos, tranformeiEmEsquilo) {
    journal.push({
        events: eventos,
        squirrel: tranformeiEmEsquilo
    });
}

function verificarSeOProcuradoEstaNoDia(dia, procurado) {
    return dia.events.indexOf(procurado) != -1;
}

function montarRelacao(procurado, journal) {

    var detalhes = [0, 0, 0, 0];

    for(var i=0; i<journal.length; i++) {
        var indice = 0;

        if(verificarSeOProcuradoEstaNoDia(journal[i], procurado)){
            indice += 1;
        }
        if(journal[i].squirrel) {
            indice += 2;
        }
        detalhes[indice] += 1;
    }
    return detalhes;
}

function verificarCorrelacao(detalhes) {
    return (detalhes[3] * detalhes[0] - detalhes[2] * detalhes[1]) / 
    Math.sqrt((detalhes[0] + detalhes[1]) * 
              (detalhes[2] + detalhes[3]) * 
              (detalhes[0] + detalhes[2]) * 
              (detalhes[1] + detalhes[3]));
}
