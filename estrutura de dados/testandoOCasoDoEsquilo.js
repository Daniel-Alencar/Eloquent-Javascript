function gatherColetations(journal) {
    var phis = {};

    for(var i = 0; i<journal.length; i++) {
        var events = journal[i].events;

        for(var j = 0; j<events.length; j++) {
            var event = events[j];
            if(!(event in phis)) {
                phis[event] = verificarCorrelacao(montarRelacao(event, journal));
            }
        }
    }
    return phis;
}

